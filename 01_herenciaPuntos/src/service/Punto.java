package service;

public class Punto {
	private int x=5;
	private int y=10;
	public Punto(int x, int y) {
		this.x=x;
		this.y=y;
	}
	public int getX() {
		return x;
	}
	public void setX(int x) {
		this.x = x;
	}
	public int getY() {
		return y;
	}
	public void setY(int y) {
		this.y = y;
	}
	public Punto() {
		super();
	}

	
}